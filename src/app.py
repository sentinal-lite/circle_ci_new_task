def divide(a,b):
    return a // b

def multiply(a,b):
    return a * b

def subtract(a,b):
    return a - b

def add(a,b):
    return a + b

def main():
    a = 10
    b = 5
    print(add(a, b))
    print(subtract(a, b))
    print(multiply(a, b))
    print(divide(a, b))


if __name__ == "__main__":
    main()